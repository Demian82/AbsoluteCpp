/*정수의 숫자들의 합을 출력하는 프로그램을 작성하라.
예를 들어 만약 입력된 숫자가 12349라면 숫자들의 합은 9 + 4 + 3 + 2 + 1 = 19이다.*/

#include <iostream>
using namespace std;

int main()
{
    int num, sum=0;

    cout << "Enter num : ";
    cin >> num;

    do
    {
        sum = num % 10;
        num = num / 10;
    } while (num > 0);

    cout << "Sumation : " << sum << endl;

    return 0;
}